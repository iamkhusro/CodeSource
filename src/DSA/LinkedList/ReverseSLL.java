package DSA.LinkedList;

public class ReverseSLL {

    public static Node reverseSLL(Node head) {
        Node prev = null;
        Node current = head;
        Node next = null;

        while (current != null) {
            next = current.next;
            current.next = prev;
            prev = current;
            current = next;
        }

        return prev;
    }

    //main
}
